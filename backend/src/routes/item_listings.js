/**
 *  @swagger
 *  /api/item_listings:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Item_listings]
 *      summary: Get all item_listings
 *      description: Get all item_listings
 *      responses:
 *        200:
 *          description: Item_listings list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Item_listings"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */
router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;

    const payload = await Item_listingsDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = [
        'id',
        'title',
        'description',
        'listing_id',
        'price',
        'expiration_date',
      ];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

